  <h1>Methods</h1>
  <p>CivicWatch is a dated snapshot, not a live feed. The restored corpus covers January 1, 2020 through December 31, 2024 and contains 22.2M posts, 5,927 legislators, and 22 topic categories.</p>
  <p>Aggregations are served from PostgreSQL base tables, restored materialized views, and local exploration views prepared after restore. Missing legislator metadata is shown as a no-data marker rather than silently excluded.</p>
  <p>The API returns snapshot metadata with every analytical response. Topic 999 is labeled Uncategorized and remains visible by default.</p>
