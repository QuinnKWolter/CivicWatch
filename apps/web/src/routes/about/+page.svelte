  <h1>About CivicWatch</h1>
  <p>CivicWatch is an exploration surface for public statements by U.S. state legislators. It is designed for citizens first, with enough depth for journalists and researchers to reproduce cuts and export data.</p>
  <p>This local build follows the Roll Call visual direction: warm paper, restrained party color, dense information, and careful caveats where fields are incomplete.</p>
